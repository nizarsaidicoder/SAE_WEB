<script>
  export default {
    data() {
      return {
        observer: null,
      };
    },
    created() {
      this.observer = new IntersectionObserver(this.onElementObserved, {
        root: null,
        rootMargin: "0px",
        threshold: 0.3, // Adjust this threshold as needed
      });
    },
    mounted() {
      this.observeSections();
      this.emitAllIds();
    },

    beforeDestroy() {
      this.observer.disconnect();
    },
    methods: {
      observeSections() {
        this.$el.querySelectorAll("section[id]").forEach((section) => {
          this.observer.observe(section);
          section.querySelectorAll("div[id]").forEach((subsection) => {
            this.observer.observe(subsection);
          });
        });
      },
      onElementObserved(entries) {
        const activeSections = [];
        entries.forEach(({ target, isIntersecting }) => {
          const id = target.getAttribute("id");
          if (isIntersecting) {
            activeSections.push(id);
          }
        });
        this.$emit("active-section-update", activeSections);
      },
      emitAllIds() {
        const sections = [];
        this.$el.querySelectorAll("section[id]").forEach((section) => {
          const id = { section: section.getAttribute("id"), subSections: [] };
          section.querySelectorAll("div[id]").forEach((subsection) => {
            const subId = subsection.getAttribute("id");
            id.subSections.push(subId);
          });
          sections.push(id);
        });
        this.$emit("all-ids", sections);
      },
    },
  };
</script>

